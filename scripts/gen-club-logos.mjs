/**
 * gen-club-logos.mjs
 * Fetches API-Football team logos for every league in the catalog and writes
 * data/club-logos.ts as a Record<normalizedClubName, logoUrl>. Used for club
 * crests in the rumour card, comparador, and league pages. ~40 API calls.
 *
 * Run: node scripts/gen-club-logos.mjs   (--dry to preview)
 */
import { readFileSync, writeFileSync } from 'fs'
import { setTimeout as sleep } from 'timers/promises'

function loadEnv(path) {
  try {
    for (const line of readFileSync(path, 'utf8').split('\n')) {
      const m = line.match(/^\s*([A-Z0-9_]+)\s*=\s*(.*)\s*$/)
      if (m && !process.env[m[1]]) process.env[m[1]] = m[2].replace(/^["']|["']$/g, '')
    }
  } catch {}
}
loadEnv('.env.local')
const KEY = process.env.API_FOOTBALL_KEY
if (!KEY) { console.error('Missing API_FOOTBALL_KEY'); process.exit(1) }
const BASE = 'https://v3.football.api-sports.io'
const SEASON = 2025
const DRY = process.argv.includes('--dry')

const LEAGUE_IDS = [
  140, 39, 78, 135, 61, 94, 203, 197,
  88, 144, 179, 207, 218, 106, 119, 113, 103,
  40, 79, 136, 62, 141, 95, 204, 199, 89, 180,
  253, 262, 71, 128, 265, 239, 268,
  98, 292, 169, 188, 307,
]

const norm = s => (s ?? '').normalize('NFD').replace(/[̀-ͯ]/g, '').toLowerCase()
  .replace(/[^a-z0-9]+/g, ' ').trim()

async function fetchJson(path) {
  const r = await fetch(BASE + path, { headers: { 'x-apisports-key': KEY } })
  if (!r.ok) throw new Error(`${r.status} ${path}`)
  return r.json()
}

const map = {}
let calls = 0
for (const lid of LEAGUE_IDS) {
  try {
    const j = await fetchJson(`/teams?league=${lid}&season=${SEASON}`)
    calls++
    for (const row of j.response ?? []) {
      const t = row.team
      if (t?.name && t?.logo) {
        const k = norm(t.name)
        if (!map[k]) map[k] = t.logo
      }
    }
    await sleep(700)
  } catch (e) {
    console.error(`league ${lid}: ${e.message}`)
  }
}
console.log(`\n${Object.keys(map).length} club logos in ${calls} calls`)
if (DRY) { console.log('sample:', Object.entries(map).slice(0, 5)); process.exit(0) }

const header = `// Auto-generated by scripts/gen-club-logos.mjs — club name (normalized) → API-Football logo URL.\n` +
  `// Refresh: node scripts/gen-club-logos.mjs\n\n` +
  `const CLUB_LOGOS: Record<string, string> = `
const body = JSON.stringify(map, null, 0) + '\n\n' +
  `function normalizeClub(s: string): string {\n` +
  `  return (s ?? '').normalize('NFD').replace(/[\\u0300-\\u036f]/g, '').toLowerCase().replace(/[^a-z0-9]+/g, ' ').trim()\n}\n\n` +
  `/** Returns the API-Football crest URL for a club name, or undefined. */\n` +
  `export function clubLogo(club?: string): string | undefined {\n` +
  `  if (!club) return undefined\n` +
  `  return CLUB_LOGOS[normalizeClub(club)]\n}\n`
writeFileSync('data/club-logos.ts', header + body)
console.log('Wrote data/club-logos.ts')
