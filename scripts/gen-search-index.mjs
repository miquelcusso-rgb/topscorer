/**
 * gen-search-index.mjs
 *
 * Builds a SERVER-ONLY, lightweight search index over ALL players (apps >= 1)
 * in the covered leagues — NOT capped by minutes like data/players-generated.ts.
 * This powers the top-bar search so any real first-division player (Gavi,
 * Fermín, rotation players…) shows up, while the heavy stats dataset stays
 * capped to keep the client bundle small. Only minimal fields are kept.
 *
 * Output: data/search-index.ts  (imported only by server code: /api/search +
 * the player profile page resolver — never by a client component).
 *
 * Run:
 *   node scripts/gen-search-index.mjs --dry   # counts only
 *   node scripts/gen-search-index.mjs         # write data/search-index.ts
 */
import { readFileSync, writeFileSync } from 'fs'
import { setTimeout as sleep } from 'timers/promises'

function loadEnv(path) {
  try {
    for (const line of readFileSync(path, 'utf8').split('\n')) {
      const m = line.match(/^\s*([A-Z0-9_]+)\s*=\s*(.*)\s*$/)
      if (m && !process.env[m[1]]) process.env[m[1]] = m[2].replace(/^["']|["']$/g, '')
    }
  } catch {}
}
loadEnv('.env.local')

const KEY = process.env.API_FOOTBALL_KEY
if (!KEY) { console.error('Missing API_FOOTBALL_KEY'); process.exit(1) }

const BASE = 'https://v3.football.api-sports.io'
const SEASON = 2025
const DRY = process.argv.includes('--dry')

// Same league set as backfill-full.mjs (keep them in sync).
const LEAGUES = [
  [140, 'La Liga'], [39, 'Premier League'], [78, 'Bundesliga'], [135, 'Serie A'], [61, 'Ligue 1'],
  [94, 'Primeira Liga'], [203, 'Süper Lig'], [197, 'Super League'], [88, 'Eredivisie'],
  [144, 'Pro League'], [179, 'Premiership'], [207, 'Swiss Super League'], [218, 'Austrian Bundesliga'],
  [106, 'Ekstraklasa'], [119, 'Superligaen'], [113, 'Allsvenskan'], [103, 'Eliteserien'],
  [253, 'MLS'], [262, 'Liga MX'], [71, 'Brasileirão Serie A'], [128, 'Liga Profesional'],
  [265, 'Primera División (Chile)'], [239, 'Categoría Primera A'],
]

const POS = { Goalkeeper: 'GK', Defender: 'DF', Midfielder: 'MF', Attacker: 'FW' }
// API-Football sometimes HTML-encodes apostrophes etc. in names (e.g. "O&apos;Reilly").
const dec = s => (s ?? '').replace(/&apos;/g, "'").replace(/&#39;/g, "'").replace(/&amp;/g, '&').replace(/&quot;/g, '"')
const nrmN = s => (s ?? '').toLowerCase().normalize('NFD').replace(/[̀-ͯ]/g, '').replace(/[.'’-]/g, ' ').replace(/\s+/g, ' ').trim()
const num = v => (v == null ? undefined : Number(v))

async function apiGet(path) {
  for (let attempt = 0; attempt < 4; attempt++) {
    const res = await fetch(`${BASE}${path}`, { headers: { 'x-apisports-key': KEY } })
    if (res.status === 429) { await sleep(2000); continue }
    if (!res.ok) throw new Error(`${res.status} on ${path}`)
    return res.json()
  }
  throw new Error(`retries exhausted on ${path}`)
}

async function leaguePlayers(leagueId, leagueName) {
  const out = []
  let page = 1, total = 1
  do {
    const data = await apiGet(`/players?league=${leagueId}&season=${SEASON}&page=${page}`)
    total = data.paging?.total ?? 1
    for (const row of data.response ?? []) {
      const st = row.statistics?.[0]
      if (!st) continue
      const apps = num(st.games?.appearences)
      if (!apps || apps < 1) continue
      const p = row.player
      const full = [p.firstname, p.lastname].filter(Boolean).join(' ').trim()
      const fullName = full && nrmN(full) !== nrmN(p.name) ? full : undefined
      out.push({
        id: num(p.id),
        name: dec(p.name),
        fullName: dec(fullName) || undefined,
        club: st.team?.name ?? '',
        league: leagueName,
        photo: p.photo,
        pos: POS[st.games?.position] ?? undefined,
        age: num(p.age),
        mins: num(st.games?.minutes) ?? 0,
      })
    }
    page++
    await sleep(220)
  } while (page <= total)
  return out
}

function strip(o) {
  const r = {}
  for (const k in o) if (o[k] !== undefined && o[k] !== null) r[k] = o[k]
  return r
}

const all = []
for (const [id, name] of LEAGUES) {
  try {
    const players = await leaguePlayers(id, name)
    all.push(...players)
    console.error(`✓ ${name} (${id}): ${players.length}`)
  } catch (e) {
    console.error(`✗ ${name} (${id}): ${e.message}`)
  }
}

// Dedup by API id — a mid-season transfer appears in two leagues; keep the one
// with more minutes (their main club this season).
const byId = new Map()
for (const p of all) {
  if (p.id == null) continue
  const cur = byId.get(p.id)
  if (!cur || (p.mins ?? 0) > (cur.mins ?? 0)) byId.set(p.id, p)
}
const deduped = [...byId.values()].map(strip)
console.error(`\nTOTAL ${deduped.length} players`)

if (DRY) { console.error('[dry] no write'); process.exit(0) }

const header = `// AUTO-GENERATED by scripts/gen-search-index.mjs — do not edit by hand.
// SERVER-ONLY lightweight search index over ALL players (apps >= 1) in the
// covered first divisions. Imported only by /api/search + the profile resolver,
// never by a client component (keeps the client bundle small).
export interface SearchIndexPlayer {
  id: number
  name: string
  fullName?: string
  club: string
  league: string
  photo?: string
  pos?: 'GK' | 'DF' | 'MF' | 'FW'
  age?: number
  mins?: number
}

export const SEARCH_INDEX: SearchIndexPlayer[] = JSON.parse(${JSON.stringify(JSON.stringify(deduped))}) as SearchIndexPlayer[]
`
writeFileSync('data/search-index.ts', header)
console.error(`✓ wrote data/search-index.ts (${deduped.length} players)`)
