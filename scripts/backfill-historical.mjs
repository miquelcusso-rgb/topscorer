/**
 * backfill-historical.mjs
 *
 * Backfill historical seasons 19/20 → 10/11 of the Big-5 leagues into
 * data/players-generated.ts (or a separate data/players-historical.ts).
 *
 * Why a separate script vs. extending gen-players.mjs:
 *  - Quota: 5 leagues × 10 seasons × 2 endpoints = 100 calls. Still fine, but
 *    we want a dry-run + per-season log + resume capability.
 *  - Output volume: ~10 × 500 players = 5,000 rows. Big diff vs. current
 *    1,300. Keep historical in its own file to keep main bundle reasonable.
 *
 * Run:
 *   node scripts/backfill-historical.mjs --dry              # show plan + first call
 *   node scripts/backfill-historical.mjs --seasons=1920,1819
 *   node scripts/backfill-historical.mjs                    # full backfill
 *
 * !! READ BEFORE RUNNING:
 * - You'll consume ~100 API-Football calls (Pro: 7,500/day budget). Fine.
 * - You'll touch data/players-historical.ts (created if missing).
 * - Don't run on the same day as gen-players.mjs unless quota allows.
 */

import { readFileSync, writeFileSync, existsSync } from 'fs'
import { setTimeout as sleep } from 'timers/promises'

function loadEnv(path) {
  try {
    for (const line of readFileSync(path, 'utf8').split('\n')) {
      const m = line.match(/^\s*([A-Z0-9_]+)\s*=\s*(.*)\s*$/)
      if (m && !process.env[m[1]]) process.env[m[1]] = m[2].replace(/^["']|["']$/g, '')
    }
  } catch {}
}
loadEnv('.env.local')

const KEY = process.env.API_FOOTBALL_KEY
if (!KEY) { console.error('Missing API_FOOTBALL_KEY'); process.exit(1) }

const BASE = 'https://v3.football.api-sports.io'
const DRY  = process.argv.includes('--dry')
const SEASON_ARG = process.argv.find(a => a.startsWith('--seasons='))?.split('=')[1]

// Big-5 leagues only for historical backfill (most-requested by users).
const LEAGUES = [
  { id: 140, name: 'La Liga' },
  { id:  39, name: 'Premier League' },
  { id:  78, name: 'Bundesliga' },
  { id: 135, name: 'Serie A' },
  { id:  61, name: 'Ligue 1' },
]

// Mapping API season (start year) → site season string.
const SEASON_MAP = {
  2019: '1920', 2018: '1819', 2017: '1718', 2016: '1617', 2015: '1516',
  2014: '1415', 2013: '1314', 2012: '1213', 2011: '1112', 2010: '1011',
}

const TARGET_SITE_SEASONS = SEASON_ARG ? SEASON_ARG.split(',') : Object.values(SEASON_MAP)

async function fetchJson(path) {
  const r = await fetch(BASE + path, { headers: { 'x-apisports-key': KEY } })
  if (!r.ok) throw new Error(`${r.status} ${path}`)
  return r.json()
}

function buildRow({ player, statistics, leagueName, siteSeason, tab }) {
  const s = statistics?.[0] ?? {}
  return {
    name:   player.name,
    club:   s.team?.name ?? '',
    league: leagueName,
    age:    player.age ?? 0,
    pj:     s.games?.appearences ?? 0,
    goles:  s.goals?.total ?? 0,
    asist:  s.goals?.assists ?? 0,
    season: siteSeason,
    src:    'live',
    tab,    // 's' or 'a'
    nationality: player.nationality,
    photo:  player.photo,
    rating: s.games?.rating ? Number(parseFloat(s.games.rating).toFixed(2)) : undefined,
  }
}

const out = []
let calls = 0

for (const [apiSeason, siteSeason] of Object.entries(SEASON_MAP)) {
  if (!TARGET_SITE_SEASONS.includes(siteSeason)) continue
  console.log(`\n=== ${siteSeason} (API season ${apiSeason}) ===`)
  for (const lg of LEAGUES) {
    if (DRY && calls > 0) break  // dry: just 1 call total to verify connectivity
    for (const ep of ['topscorers', 'topassists']) {
      try {
        const j = await fetchJson(`/players/${ep}?league=${lg.id}&season=${apiSeason}`)
        calls++
        const tab = ep === 'topscorers' ? 's' : 'a'
        for (const r of j.response ?? []) {
          out.push(buildRow({ ...r, leagueName: lg.name, siteSeason, tab }))
        }
        console.log(`  ${lg.name} ${ep}: +${j.response?.length ?? 0} rows`)
        if (DRY) break
        await sleep(800)
      } catch (e) {
        console.error(`  ${lg.name} ${ep}: ${e.message}`)
      }
    }
  }
}

console.log(`\n${calls} API calls · ${out.length} rows total\n`)

if (DRY) {
  console.log('--dry: no file write.')
  console.log('Sample row:', JSON.stringify(out[0] ?? null, null, 2))
  process.exit(0)
}

const path = 'data/players-historical.ts'
const header = `// Auto-generated by scripts/backfill-historical.mjs on ${new Date().toISOString().slice(0, 10)}\n` +
               `import type { PlayerData } from '@/types'\n\n` +
               `export const HISTORICAL_PLAYERS: PlayerData[] = `
const body = JSON.stringify(out, null, 2) + ' as unknown as PlayerData[]\n'
writeFileSync(path, header + body)
console.log(`Wrote ${path} (${out.length} rows)`)

console.log(`
Next step (manual): merge into data/players.ts:

  import { HISTORICAL_PLAYERS } from './players-historical'
  export const PLAYERS: PlayerData[] = [...CURATED, ...GENERATED, ...HISTORICAL_PLAYERS]

Then rebuild and verify the season dropdown shows the new years.
`)
